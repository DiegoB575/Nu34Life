package com.app.app_nu34life.retrofit;

public class NutrFact {

    private Integer id;
    private int  carbohydrates;
    private int  energeticValue;
    private int  protein;
    private int  salt;
    private int  saturatedFats;
    private int  sugars;
    private int  totalFat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCarbohydrates() {
        return carbohydrates;
    }

    public void setCarbohydrates(int carbohydrates) {
        this.carbohydrates = carbohydrates;
    }

    public int getEnergeticValue() {
        return energeticValue;
    }

    public void setEnergeticValue(int energeticValue) {
        this.energeticValue = energeticValue;
    }

    public int getProtein() {
        return protein;
    }

    public void setProtein(int protein) {
        this.protein = protein;
    }

    public int getSalt() {
        return salt;
    }

    public void setSalt(int salt) {
        this.salt = salt;
    }

    public int getSaturatedFats() {
        return saturatedFats;
    }

    public void setSaturatedFats(int saturatedFats) {
        this.saturatedFats = saturatedFats;
    }

    public int getSugars() {
        return sugars;
    }

    public void setSugars(int sugars) {
        this.sugars = sugars;
    }

    public int getTotalFat() {
        return totalFat;
    }

    public void setTotalFat(int totalFat) {
        this.totalFat = totalFat;
    }
}
